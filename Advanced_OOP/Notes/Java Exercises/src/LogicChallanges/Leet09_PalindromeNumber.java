package LogicChallanges;

public class Leet09_PalindromeNumber {
    public static void main(String[] args){
        System.out.println(isPalindrome(101));
    }

    public static boolean isPalindrome(int x){
        if(x < 0)
            return false;
        else{
            System.out.println(x);
        }

        return true;
    }
}
